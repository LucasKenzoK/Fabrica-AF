package model;

public class Colaborador {
//Atributos
public String nome;
public String senha;
public String email;
//GET AND SET

public String getNome(){
return nome;    
}
public void setNome(String nome){
this.nome = nome;    
}
public String getSenha(){
return senha;    
}
public void setSenha(String senha){
this.senha = senha;    
}
public String getEmail(){
return email;    
}
public void setEmail(String email){
this.email = email;
}
}